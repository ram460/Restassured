package PojoclassDemo;

public class Userlibrary 
{
private String name;
 private String job;
private String id;
public Userlibrary(String name, String job, String id) {
	super();
	this.name = name;
	this.job = job;
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getJob() {
	return job;
}
public void setJob(String job) {
	this.job = job;
}
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}


}
